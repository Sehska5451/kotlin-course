fun Temperature (Temperature: Double, Scale: String) {
    if(Scale == "Celsius") {
        Temperature = (Temperature * (9/5)) + 32
        println(Temperature)print("F")
    } else if(Scale == "Fahrenheit") {
        Temperature = (Temperature - 32) * (5/9)
        println(Temperature)print("C")
    } else {
        println("Error")
    }
}

